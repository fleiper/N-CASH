CREATE DATABASE NCASH_BANCO2_1e;
USE NCASH_BANCO1e;

CREATE TABLE DEPARTAMENTO(
CODIGO_DEPARTAMENTO INT auto_increment primary key,
DEPARTAMENTO_NOME VARCHAR(50)
);

CREATE TABLE FUNCIONARIO (
CODIGO_FUNCIONARIO INT auto_increment primary key,
CODIGO_DEPARTAMENTO INT,
NOME VARCHAR (50),
NASCIMENTO date,
SALARIO DECIMAL (10,2),
foreign key (CODIGO_DEPARTAMENTO) references DEPARTAMENTO (CODIGO_DEPARTAMENTO)
);
 
CREATE TABLE DEPENDENTE(
CODIGO_DEPENDENTE INT auto_increment primary key,
CODIGO_FUNCIONARIO INT,
DEPENDENTE_NOME VARCHAR (50),
foreign key (CODIGO_FUNCIONARIO) references FUNCIONARIO (CODIGO_FUNCIONARIO)
);
